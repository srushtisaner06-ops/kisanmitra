/* ── Translation helper ── */
window.KP_I18N = {
  /**
   * Get current language code from localStorage (defaults to 'en').
   */
  lang: function () {
    return localStorage.getItem('kp-lang') || 'en';
  },

  /**
   * Translate a key into the current language.
   * Falls back to English if the key/lang is missing.
   */
  t: function (key) {
    var lang = this.lang();
    var entry = window.KP_TRANSLATIONS[key];
    if (!entry) return key;
    return entry[lang] || entry['en'] || key;
  },

  /**
   * Apply all data-i18n="key" elements on the page.
   * Also updates placeholder, title, and aria-label attributes.
   */
  apply: function () {
    var lang = this.lang();
    var self = this;

    /* --- Static text nodes --- */
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var text = self.t(key);
      // Only update textContent — don't destroy child elements
      if (el.children.length === 0) {
        el.textContent = text;
      } else {
        // If element has child elements, update first text node only
        for (var i = 0; i < el.childNodes.length; i++) {
          if (el.childNodes[i].nodeType === 3 && el.childNodes[i].textContent.trim()) {
            el.childNodes[i].textContent = text + ' ';
            break;
          }
        }
      }
    });

    /* --- Placeholder attributes --- */
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      el.placeholder = self.t(el.getAttribute('data-i18n-ph'));
    });

    /* --- Title attributes --- */
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      el.title = self.t(el.getAttribute('data-i18n-title'));
    });

    /* --- Update <html lang=""> attribute --- */
    document.documentElement.lang = lang;

    /* --- Notify dynamic renderers that language changed --- */
    document.dispatchEvent(new CustomEvent('kp-lang-changed', { detail: { lang: lang } }));
  }
};
